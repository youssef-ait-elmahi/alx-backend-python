#!/usr/bin/env python3
"""
This module contains a function that adds two float numbers.
"""


def add(a: float, b: float) -> float:
    """
    Function to add two float numbers.

    Parameters:
    a (float): The first number.
    b (float): The second number.

    Returns:
    float: The sum of a and b.
    """

    return a + b
